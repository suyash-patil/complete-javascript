/*
There are 3 types of it:
Logical OR (||)
Logical AND (&&)
Logical NOT (!)
*/

let firstCondition = true;
let secondCondition = false;

console.log(firstCondition || secondCondition); // true as one of the condition is true

console.log(firstCondition && secondCondition); // false, as both needs to be true to get the result as true

console.log(!firstCondition); // opposite of true is false

